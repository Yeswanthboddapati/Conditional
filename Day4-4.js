var num1=+prompt("enter the num1");
var num2=+prompt("enter the num2");
var opp=prompt("enter the operator");
switch(opp){
   
    case "+":
        console.log(num1+num2);
        break;
    case "-":
        console.log(num1-num2);
        break;
    case "*":
        console.log(num1*num2);
        break;
    case "/":
        console.log(num1/num2);
        break;
    case "%":
        console.log(num1%num2);
        break;
    case "**":
        console.log(num1**num2);
        break;
}